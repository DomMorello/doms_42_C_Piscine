int	ft_str_is_numeric(char *str)
{
	int cnt;

	cnt = 0;
	if (str[cnt] == '\0')
		return (1);
	while (str[cnt] != '\0')
	{
		if (!(str[cnt] >= '0' && str[cnt] <= '9'))
		{
			return (0);
		}
		cnt++;
	}
	return (1);
}
