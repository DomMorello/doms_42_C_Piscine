int	ft_any(char **tab, int (*f)(char*))
{
	int i;
	int len;

	len = 0;
	while (tab[len])
		len++;
	i = 0;
	while (i < len)
	{
		if (f(tab[i]) != 0)
			return (1);
		i++;
	}
	return (0);
}
